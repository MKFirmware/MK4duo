#define CODE_M111

/**
 * M111: Debug mode Repetier Host compatibile
 */
inline void gcode_M111(void) {
  if (parser.seenval('S'))
    printer.setDebugLevel(parser.value_byte());
  if (parser.seenval('P')) {
    const int flag = parser.value_int();
    if (flag > 0)
      printer.debugSet(flag);
    else
      printer.debugReset(-flag);
  }
}
